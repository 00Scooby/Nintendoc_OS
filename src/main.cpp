void setup() {
  Serial.begin(9600);
  delay(250); // Gib dem Board kurz Zeit, die VCC Spannung zu stabilisieren
  
  // 1. Display starten
  if(!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) {
    for(;;); 
  }

  // Puffer leeren, damit das Rauschen verschwindet
  display.clearDisplay();
  display.display();

  // 2. Sensor starten
  if (!ina219.begin()) {
    Serial.println("INA219 nicht gefunden");
  }

  // BOOT-SCREEN: Dein Logo anzeigen
  display.clearDisplay();
  display.drawBitmap(0, 0, nintendoc_logo, 128, 64, SSD1306_WHITE);
  display.display();
  
  delay(2000); // 3 Sekunden dein Logo geniessen

  // NEU: Der Text-Splashscreen
  display.clearDisplay();
  display.setTextSize(2);
  display.setTextColor(SSD1306_WHITE);

  // Zeile 1: "Nintendoc" (10 Pixel vom linken Rand zentriert)
  display.setCursor(10, 5);
  display.print(F("Nintendoc"));

  // Zeile 2: "Charging" (16 Pixel vom linken Rand)
  display.setCursor(16, 25);
  display.print(F("Charging"));

  // Zeile 3: "Board" (34 Pixel vom linken Rand)
  display.setCursor(34, 45);
  display.print(F("Board"));

  display.display();
  delay(3000); // Den Text 2 Sekunden lang anzeigen

  // SAUBERER ÜBERGANG zum Loop:
  display.clearDisplay();
  display.display();
  delay(100); 
}

void loop() {
  // 1. Rohdaten vom Sensor
  float raw_voltage = ina219.getBusVoltage_V();
  float current_mA = ina219.getCurrent_mA();
  float power_mW = ina219.getPower_mW();

  // 2. Der Software-Stossdämpfer (Glättung)
  static float smoothed_voltage = 0.0;
  if (smoothed_voltage == 0.0) {
    smoothed_voltage = raw_voltage;
  } else {
    smoothed_voltage = (smoothed_voltage * 0.8) + (raw_voltage * 0.2);
  }

  // 3. Akku-Prozent berechnen
  int percent = 0;
  if (smoothed_voltage >= 4.20) {
    percent = 100;
  } else if (smoothed_voltage <= 3.00) {
    percent = 0;
  } else {
    percent = (smoothed_voltage - 3.00) / (4.20 - 3.00) * 100;
  }

  // 4. Der Live-Kapazitätszähler (Coulomb Counter)
  static float charged_mAh = 0.0;
  static unsigned long lastChargeUpdate = 0;
  unsigned long currentMillis = millis();
  
  if (lastChargeUpdate == 0) lastChargeUpdate = currentMillis;
  unsigned long deltaMs = currentMillis - lastChargeUpdate;
  lastChargeUpdate = currentMillis;

  // Wenn geladen wird (> 50mA), Kapazität aufaddieren
  if (current_mA > 50) {
    charged_mAh += (current_mA * (deltaMs / 3600000.0)); 
  }

  // 5. Status-Logik (DER RAM-FIX: const char* statt String!)
  const char* status = "Standby";
  if (current_mA > 50) {
    status = "Laden...";
  } else if (current_mA < -50) {
    status = "Entladen"; 
  } else if (smoothed_voltage >= 4.15 && current_mA < 50) {
    status = "Akku Voll";
  }

  // 6. Timer für den Seitenwechsel (alle 5 Sekunden)
  static unsigned long lastPageSwitch = 0;
  static int page = 0;
  
  if (currentMillis - lastPageSwitch >= 5000) {
    page = !page; // Wechselt zwischen 0 und 1
    lastPageSwitch = currentMillis;
  }

  // 7. Display aufbauen
  display.clearDisplay();
  display.setTextColor(SSD1306_WHITE);
  
  // Header (Immer sichtbar & zentriert)
  display.setTextSize(1);
  display.setCursor(13, 0); // Exakt zentriert für 17 Zeichen
  display.print(F("Nintendoc Charger"));
  display.drawFastHLine(0, 10, 128, SSD1306_WHITE);

  // Grosse Anzeige: Spannung & Prozent (Immer sichtbar)
  display.setTextSize(2);
  display.setCursor(0, 15);
  display.print(smoothed_voltage); 
  display.print(F("V"));
  
  display.setCursor(85, 15);
  display.print(percent);
  display.print(F("%"));

  // ROTIERENDER BEREICH (Zeile 35 und 45)
  display.setTextSize(1);
  if (page == 0) {
    // Seite 1: Live-Daten
    display.setCursor(0, 35);
    display.print(F("Strom: "));
    display.print(current_mA, 0); 
    display.print(F(" mA"));

    display.setCursor(0, 45);
    display.print(F("Power: "));
    display.print(power_mW, 0);
    display.print(F(" mW"));
  } else {
    // Seite 2: Der neue Kapazitätszähler
    display.setCursor(0, 35);
    display.print(F("Eingeladen:"));
    
    display.setCursor(0, 45);
    display.print(charged_mAh, 1); 
    display.print(F(" mAh"));
  }

  // Status (Immer sichtbar ganz unten)
  display.setCursor(0, 55);
  display.print(F("Status: "));
  display.print(status);

  display.display();
  delay(250);
}