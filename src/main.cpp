// Hilfsfunktion für die Prozentberechnung
int get_battery_percent(float v) {
  if (v >= 4.15) return 100;
  if (v >= 4.00) return 85;
  if (v >= 3.90) return 75;
  if (v >= 3.80) return 50;
  if (v >= 3.70) return 25;
  if (v >= 3.60) return 10;
  if (v >= 3.30) return 5;
  return 0;
}

void setup() {
  Serial.begin(9600);
  delay(250); 

  // 1. Display starten
  if(!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) {
    for(;;); 
  }
  display.clearDisplay();
  display.display();

  // 2. Sensor starten
  if (!ina219.begin()) {
    Serial.println(F("INA219 not found"));
  }

  // BOOT-SCREEN
  display.clearDisplay();
  display.drawBitmap(0, 0, nintendoc_logo, 128, 64, SSD1306_WHITE);
  display.display();
  delay(2000); 

  // Text-Splashscreen
  display.clearDisplay();
  display.setTextSize(2);
  display.setTextColor(SSD1306_WHITE);
  display.setCursor(10, 5);
  display.print(F("Nintendoc"));
  display.setCursor(16, 25);
  display.print(F("Charging"));
  display.setCursor(34, 45);
  display.print(F("Board"));
  display.display();
  delay(3000); 

  display.clearDisplay();
  display.display();
  delay(100); 
}

void loop() {
  unsigned long currentMillis = millis();

  // HIGHSPEED UPDATE: Sensordaten holen
  float raw_voltage = ina219.getBusVoltage_V();
  float raw_current = ina219.getCurrent_mA();
  float raw_power = ina219.getPower_mW();

  // Noise-Gate: Rauschen um den Nullpunkt killen
  if (raw_current < 0.0) raw_current = 0.0;
  if (raw_power < 0.0) raw_power = 0.0;

  // --- DER SOFTWARE-STOSSDAEMPFER FÜR ALLE WERTE (EXTRA SOFT) ---
  static float smoothed_voltage = 0.0;
  static float smoothed_current = 0.0;
  static float smoothed_power = 0.0;

  if (smoothed_voltage == 0.0) smoothed_voltage = raw_voltage;
  else smoothed_voltage = (smoothed_voltage * 0.95) + (raw_voltage * 0.05);

  if (smoothed_current == 0.0) smoothed_current = raw_current;
  else smoothed_current = (smoothed_current * 0.95) + (raw_current * 0.05);

  if (smoothed_power == 0.0) smoothed_power = raw_power;
  else smoothed_power = (smoothed_power * 0.95) + (raw_power * 0.05);

  bool is_battery_connected = (smoothed_voltage > 2.0);

  // --- DISPLAY & BERECHNUNG ---
  static unsigned long lastUpdate = 0;
  if (currentMillis - lastUpdate >= 100) {
    unsigned long deltaMs = currentMillis - lastUpdate;
    lastUpdate = currentMillis;

    // Wir nutzen jetzt den geglätteten Strom für die Logik
    bool is_charging = (smoothed_current > 10.0);
    static float charged_mAh = 0.0;

    // Reset mAh, wenn Akku abgesteckt wird
    if (!is_battery_connected) {
      charged_mAh = 0.0; 
    }

    // Coulomb Counter
    if (is_charging) {
      charged_mAh += (smoothed_current * (deltaMs / 3600000.0)); 
    }

    // Prozentberechnung
    int percent = get_battery_percent(smoothed_voltage);

    // Seitenwechsel
    static unsigned long lastPageSwitch = 0;
    static int page = 0;
    if (currentMillis - lastPageSwitch >= 5000) {
      page = !page; 
      lastPageSwitch = currentMillis;
    }

    // Display aufbauen
    display.clearDisplay();
    display.setTextColor(SSD1306_WHITE);

    // Header
    display.setTextSize(1);
    display.setCursor(13, 0); 
    display.print(F("Nintendoc Charger"));
    display.drawFastHLine(0, 10, 128, SSD1306_WHITE);

    // Grosse Anzeige: Spannung
    display.setTextSize(2);
    display.setCursor(0, 15);
    display.print(smoothed_voltage); 
    display.print(F("V"));

    // Dynamische Prozent-Ausrichtung
    if (percent == 100) {
      display.setCursor(75, 15); 
    } else if (percent >= 10) {
      display.setCursor(85, 15); 
    } else {
      display.setCursor(95, 15); 
    }
    display.print(percent);
    display.print(F("%"));

    // ROTIERENDER BEREICH
    display.setTextSize(1);
    if (page == 0) {
      // Seite 1: Live-Daten (jetzt mit geglätteten Werten)
      display.setCursor(0, 35);
      display.print(F("Current: "));
      display.print(smoothed_current, 0); 
      display.print(F(" mA"));

      display.setCursor(0, 45);
      display.print(F("Power:   "));
      display.print(smoothed_power, 0);
      display.print(F(" mW"));
    } else {
      // Seite 2: Counter
      display.setCursor(0, 35);
      display.print(F("Charged Cap.:"));

      display.setCursor(0, 45);
      display.print(charged_mAh, 1); 
      display.print(F(" mAh"));
    }

    // --- ANIMIERTER LADEBALKEN ---
    // Äusserer Rahmen
    display.drawRect(0, 54, 128, 10, SSD1306_WHITE); 
    
    // Innere Füllung
    if (percent > 0) {
      int barWidth = map(percent, 0, 100, 0, 124);
      display.fillRect(2, 56, barWidth, 6, SSD1306_WHITE);
      
      // Die chillige Puls-Animation beim Laden
      if (is_charging && percent < 100) {
        int offset = (currentMillis / 350) % 6;
        for (int i = 2 + offset; i < 2 + barWidth; i += 6) {
          display.drawFastVLine(i, 56, 6, SSD1306_BLACK);
        }
      }
    }

    display.display();
  }
}